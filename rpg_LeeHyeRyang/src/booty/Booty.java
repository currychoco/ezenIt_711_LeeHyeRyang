package booty;

public class Booty {
	private int exp;
	private int gold;
	
	public Booty(int exp, int gold) {
		super();
		this.exp = exp;
		this.gold = gold;
	}

	public int getExp() {
		return exp;
	}

	public void setExp(int exp) {
		this.exp = exp;
	}

	public int getGold() {
		return gold;
	}

	public void setGold(int gold) {
		this.gold = gold;
	}
	
}
